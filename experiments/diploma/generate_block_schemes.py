@dataclass(frozen=True)
class Box:
    key: str
    x: float
    y: float
    w: float
    h: float
    text: str
    color: str

    @property
    def left(self) -> tuple[float, float]:
        return (self.x, self.y + self.h / 2)

    @property
    def right(self) -> tuple[float, float]:
        return (self.x + self.w, self.y + self.h / 2)

    @property
    def top(self) -> tuple[float, float]:
        return (self.x + self.w / 2, self.y + self.h)

    @property
    def bottom(self) -> tuple[float, float]:
        return (self.x + self.w / 2, self.y)


def setup() -> None:
    OUT_DIR.mkdir(parents=True, exist_ok=True)
    plt.rcParams.update(
        {
            "font.family": "DejaVu Sans",
            "svg.fonttype": "none",
            "figure.facecolor": "white",
            "axes.facecolor": "white",
        }
    )


def draw_box(ax: plt.Axes, box: Box, fontsize: int = 12) -> None:
    patch = FancyBboxPatch(
        (box.x, box.y),
        box.w,
        box.h,
        boxstyle="round,pad=0.04,rounding_size=0.08",
        linewidth=1.45,
        edgecolor="#25313f",
        facecolor=box.color,
    )
    ax.add_patch(patch)
    ax.text(
        box.x + box.w / 2,
        box.y + box.h / 2,
        box.text,
        ha="center",
        va="center",
        fontsize=fontsize,
        color="#111827",
        linespacing=1.18,
    )


def draw_arrow(
    ax: plt.Axes,
    start: tuple[float, float],
    end: tuple[float, float],
    *,
    rad: float = 0.0,
) -> None:
    arrow = FancyArrowPatch(
        start,
        end,
        arrowstyle="-|>",
        mutation_scale=15,
        linewidth=1.35,
        color="#374151",
        connectionstyle=f"arc3,rad={rad}",
        shrinkA=5,
        shrinkB=5,
    )
    ax.add_patch(arrow)


def finish(
    fig: plt.Figure,
    ax: plt.Axes,
    name: str,
    title: str,
    xlim: tuple[float, float],
    ylim: tuple[float, float],
) -> None:
    ax.set_xlim(*xlim)
    ax.set_ylim(*ylim)
    ax.axis("off")
    ax.text(
        (xlim[0] + xlim[1]) / 2,
        ylim[1] - 0.22,
        title,
        ha="center",
        va="top",
        fontsize=15,
        weight="bold",
        color="#111827",
    )
    fig.savefig(OUT_DIR / f"{name}.png", dpi=240, bbox_inches="tight", pad_inches=0.12)
    fig.savefig(OUT_DIR / f"{name}.svg", bbox_inches="tight", pad_inches=0.12)
    plt.close(fig)


def draw_boxes(ax: plt.Axes, boxes: Iterable[Box], fontsize: int = 12) -> dict[str, Box]:
    mapping: dict[str, Box] = {}
    for box in boxes:
        draw_box(ax, box, fontsize=fontsize)
        mapping[box.key] = box
    return mapping


def draw_vertical_chain(ax: plt.Axes, boxes: dict[str, Box], keys: list[str]) -> None:
    for upper, lower in zip(keys, keys[1:]):
        draw_arrow(ax, boxes[upper].bottom, boxes[lower].top)


